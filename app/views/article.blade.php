                <h1>Sochi Olympic torch taken on historic spacewalk</h1>

                <p>Two Russian cosmonauts are taking the torch for the Sochi Winter Olympics on its first historic spacewalk, ahead of next year's games in Russia.</p>

                <p>Oleg Kotov and Sergei Ryazansky took the unlit version of the torch through the hatch of the International Space Station at 1434 GMT.</p>
                <p>Live footage showed Mr Kotov waving the torch 261 miles (420km) above Earth.</p>
                <p>The crew handed the Olympic symbol to Mr Kotov and Mr Ryazansky, who were already on the orbiting station ahead of Saturday's spacewalk.</p>
                <p>"We'd like to showcase our Olympic torch in space. We will try to do it in a beautiful manner. Millions of people will see it live on TV and they will see the station and see how we work."</p>
